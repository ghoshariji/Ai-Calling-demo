const subUserSchema = new mongoose.Schema({
    adminId: {
      type: mongoose.Schema.Types.ObjectId,
      ref: 'User',
      required: true,
    },
    name: {
      type: String,
      required: true,
    },
    email: {
      type: String,
      required: true,
      unique: true,
    },
    password: {
      type: String,
      required: true,
    },
    phone: {
      type: String,
      required: true,
    }
  }, { timestamps: true });
  
  module.exports = mongoose.model('SubUser', subUserSchema);
  